let boxes = document.querySelector(".areasdeintervecaoboxes")
let allBtns =  document.querySelectorAll('.left')
let firstDiv = boxes.querySelector(".areasparentdiv").offsetWidth;

boxes.innerHTML='';

box.forEach(elem=>{
    let createDiv = document.createElement('div')
    createDiv.classList.add('areasparentdiv')
    createDiv.innerHTML =`
        <div class="picturediv">
            <img class="areaimg" src="${elem.picture}" alt="">
        </div>
        <div class="textdiv">
            <div class="textdivtitle">
                <h1>${elem.title}</h1>
            </div>
            <div class="infoabtareas">
                ${elem.text}
            </div>
            <div class="nowmorediv">
                <a href="/areasintervencao.html">Saiba mais</a>
            </div>
        </div>
        `   
        boxes.append(createDiv)
})


let isScrolling = false, startX,startscrollLeft;
let count = 0;
let countb = box.length;

allBtns.forEach(elem =>{
    elem.addEventListener("click",()=>{
        boxes.scrollLeft += elem.id ==="left"? (-firstDiv) : firstDiv      
    })
})

const autoplay = ()=>{
        if (count>=box.length-1){
            boxes.scrollLeft -=firstDiv
            countb--;
            countb==1?count=0:'';
        }
        else{
            boxes.scrollLeft += firstDiv 
            countb=box.length
        }
        count++;
}

let intervals = setInterval(autoplay,3000)

const scrolling = (e) =>{
    if (isScrolling==false) {
        return
    }
    boxes.scrollLeft = startscrollLeft - (e.pageX-startX) 
}

const scrollstart = (e) =>{
    clearInterval(intervals)
    isScrolling = true;
    boxes.classList.add('scrolling')
    startX = e.pageX
    startscrollLeft = boxes.scrollLeft
} 

const scrollend = () =>{
    isScrolling = false
    boxes.classList.remove('scrolling')
    intervals = setInterval(autoplay,3000)
}
 
boxes.addEventListener("mousemove",scrolling)
boxes.addEventListener("mousedown", scrollstart)
boxes.addEventListener("mouseup",scrollend)
